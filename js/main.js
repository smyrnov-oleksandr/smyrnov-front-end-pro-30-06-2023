// task 1 : Вивести на сторінку в один рядок через кому числа від 10 до 20.
let t1 = '';

for (let i = 10; i < 21; i++) {
  if (i == 20) {
    t1 += `${i}`;
  } else {
    t1 += `${i},`;
  }
}
alert(`task 1: Вивести на сторінку в один рядок через кому числа від 10 до 20 
${t1}`);

