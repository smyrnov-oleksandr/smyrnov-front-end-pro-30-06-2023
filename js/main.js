// task 9 : Дано натуральне число. Знайти та вивести на сторінку всі його дільники.
let t9num = +prompt('task 9. Введіть натуральне число: ');
let t9 = '';

for (let i = 0; i < t9num + 1; i++) {
  if (t9num % i == 0) {
    t9 += `${i} `;
  }
}
alert(`task 9: Дано натуральне число. Знайти та вивести на сторінку всі його дільники.
${t9}`);

// task 10 : Визначити кількість його парних дільників.
let t10 = [];
let t10arr = t9.split(' ');
t10arr.pop();

for (let i = 0; i < t10arr.length; i++) {
  if (t10arr[i] % 2 == 0) t10.push(t10arr[i]);
}
alert(`task 10: Визначити кількість його парних дільників.
${t10.length}`);

// task 11 : Знайти суму його парних дільників.
let t11 = t10.reduce((acc, item) => (acc += +item), 0);
alert(`task 11: Знайти суму його парних дільників
${t11}`);

